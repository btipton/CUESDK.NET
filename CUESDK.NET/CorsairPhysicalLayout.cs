namespace Spectrum.CUE.SDK
{
    /// <summary>
    /// Contains list of available physical layouts for keyboards.
    /// </summary>
    public enum CorsairPhysicalLayout
    {
        /// <summary>
        /// Dummy value
        /// </summary>
        CPL_Invalid = 0,

        /// <summary>
        /// For US keyboard
        /// </summary>
        CPL_US = 1,

        /// <summary>
        /// For UK keyboard
        /// </summary>
        CPL_UK = 2,

        /// <summary>
        /// For BR keyboard
        /// </summary>
        CPL_BR = 3,

        /// <summary>
        /// For JP keyboard
        /// </summary>
        CPL_JP = 4,

        /// <summary>
        /// For KR keyboard
        /// </summary>
        CPL_KR = 5,

        /// <summary>
        /// For mouse with 1 led zone
        /// </summary>
        CPL_Zones1 = 6,

        /// <summary>
        /// For mouse with 2 led zones
        /// </summary>
        CPL_Zones2 = 7,

        /// <summary>
        /// For mouse with 3 led zones
        /// </summary>
        CPL_Zones3 = 8,

        /// <summary>
        /// For mouse with 4 led zones
        /// </summary>
        CPL_Zones4 = 9
    }
}
